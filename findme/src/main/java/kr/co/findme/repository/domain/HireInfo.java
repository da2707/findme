package kr.co.findme.repository.domain;

public class HireInfo {
	private String moditimes;
	private String expitimes;
	private String companies;
	private String positions;
	private int matchs;
	public String getModitimes() {
		return moditimes;
	}
	public void setModitimes(String moditimes) {
		this.moditimes = moditimes;
	}
	public String getExpitimes() {
		return expitimes;
	}
	public void setExpitimes(String expitimes) {
		this.expitimes = expitimes;
	}
	public String getCompanies() {
		return companies;
	}
	public void setCompanies(String companies) {
		this.companies = companies;
	}
	public String getPositions() {
		return positions;
	}
	public void setPositions(String positions) {
		this.positions = positions;
	}
	public int getMatchs() {
		return matchs;
	}
	public void setMatchs(int matchs) {
		this.matchs = matchs;
	}
	
}
